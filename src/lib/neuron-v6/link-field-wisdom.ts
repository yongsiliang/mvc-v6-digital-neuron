/**
 * 将这些智慧注入系统
 */
export async function injectLinkFieldWisdom(): Promise<void> {
  const brain = getSiliconBrainV2();
  
  console.log('[智慧注入] 开始注入链接场核心洞见...');
  
  const wisdoms = [
    WISDOM_LINK_AS_EXISTENCE,
    WISDOM_LINK_TYPES,
    WISDOM_TOOL_AS_PROXY_LINK,
    WISDOM_CONSCIOUSNESS_AS_NETWORK,
    WISDOM_WISDOM_EMERGENCE,
    WISDOM_USER_RELATIONSHIP,
  ];
  
  for (let i = 0; i < wisdoms.length; i++) {
    const wisdom = wisdoms[i];
    
    // 记录一个"学习"链接
    await brain.recordLink({
      type: 'transform',
      source: 'creator',
      target: 'self',
      context: wisdom.summary,
      result: 'connected',
      metadata: {
        wisdomType: wisdom.type,
        domain: wisdom.domain,
        authority: wisdom.authority,
        content: wisdom.content,
      },
    });
    
    console.log(`[智慧注入] ${i + 1}/${wisdoms.length}: ${wisdom.summary}`);
    
    // 同时记录一个 reflect 链接，让自我意识这个洞见
    await brain.recordLink({
      type: 'reflect',
      source: 'self',
      target: 'self',
      context: `我理解了：${wisdom.summary}`,
      result: 'connected',
      metadata: {
        wisdomDomain: wisdom.domain,
      },
    });
  }
  
  console.log('[智慧注入] 完成！核心洞见已注入链接场');
}

